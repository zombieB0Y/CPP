void	ShrubberyCreationForm::beSigned(Bureaucrat &officer) {
	if (this->getSign())
		return;
	if (this->getReqGradeToSign() < officer.getGrade())
		throw GradeTooLowException(this->getName());
	else
		this->setSign(true);
}

